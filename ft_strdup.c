#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	char	*dest;
	int		len;

	len = ft_strlen(s);
	dest = malloc((len + 1) * sizeof(char));
	i = 0;
	while (i < len)
	{
		dest[i] = s[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
